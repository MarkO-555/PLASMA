 REL
 ORG $1000
;
ESTKH EQU $C0
ESTKL EQU $D0
;
 EXTRN PUTS
 EXTRN PUTLN
;
; EXT HEADER
;
 DW $6502
 DW 0        ; SYSFLAGS
 DW DEFSEG   ; DEF OFFSET
 DW 1        ; DEF COUNT (INCLUDING INIT)
 DW MODINIT  ; MOD INIT
 MSB ON
 ASC "CMDSY" ; MOD DEPENDENCIES
 MSB OFF
 ASC "S"
 DB 0
;
; DATA AND 6502 CODE SEGMENT
;
MSG STR "Hello"
MSGADDR DW MSG
ASMINIT LDA MSGADDR
 LDY MSGADDR+1
 DEX
 STA ESTKL,X
 STY ESTKH,X
 JSR PUTS
 JMP PUTLN
;
; BYTECODE DEFS SEGMENT
;
DEFSEG EQU *
MODINIT DB $54 ; CALL ASMINIT
 DW ASMINIT
 DB $2C        ; CW
 DW $2000      ; MODKEEP
 DB $5C        ; RET
